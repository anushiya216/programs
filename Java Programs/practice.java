import java.util.*;
public class practice {
    public static void main(String[] args) 
    {
        
        Scanner obj=new Scanner(System.in);
        int n=obj.nextInt();
        int i,j;
        for(i=0;i<=n;i++,System.out.println())
        {
            for(j=0;j<=n;j++)
            {
                System.out.print("*");
            }
        }

    }
    
}
